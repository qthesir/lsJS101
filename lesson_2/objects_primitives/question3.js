// What will the following code log to the console and why? 

let myWords = ['Hello'];
myWords.push('Goodbye');

console.log(myWords);

// myWords will log ['Hello', 'Goodbye'] to the console. This is because myWords is an
// Array object, and myWords.push() is a mutating method that inserts the value in 
// its parenthesis to the end of the array. 